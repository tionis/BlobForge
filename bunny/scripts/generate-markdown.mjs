import { build } from "esbuild";
import { mkdir, writeFile } from "node:fs/promises";

const result = await build({
  entryPoints: ["src/markdown_runtime.ts"],
  bundle: true,
  minify: true,
  format: "iife",
  platform: "browser",
  target: "es2022",
  write: false,
});
const source = new TextDecoder().decode(result.outputFiles[0].contents);
await mkdir("src/generated", { recursive: true });
await writeFile(
  "src/generated/markdown_bundle.ts",
  `// Generated by npm run generate:markdown. Do not edit.\nexport const MARKDOWN_JS = ${JSON.stringify(source)};\n`,
);
